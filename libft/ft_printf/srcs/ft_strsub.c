#include "ft_printf.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	unsigned	int	i;
	char			*str;

	if ((str = ft_strnew(len)) && s)
	{
		i = 0;
		while (i < len)
		{
			str[i] = s[start + i];
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
